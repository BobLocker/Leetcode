package me.boblocker;

public class Initial {
    private int val;
    private int step;
    Initial(int val, int step) {
        this.val = val;
        this.step = step;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public int getStep() {
        return step;
    }

    public void setStep(int step) {
        this.step = step;
    }
}
